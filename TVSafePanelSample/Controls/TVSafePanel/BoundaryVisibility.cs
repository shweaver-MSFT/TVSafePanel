namespace TVSafePanelSample.Controls
{
    /// <summary>
    /// Determines how a Boundary should be applied.
    /// </summary>
    public enum BoundaryVisibility
    {
        /// <summary>
        /// Displays boundary automatically based on platform
        /// </summary>
        Auto,

        /// <summary>
        /// Boundary is displayed
        /// </summary>
        Visible,

        /// <summary>
        /// Boundary is not displayed
        /// </summary>
        Collapsed
    }
}
